module Z =
 struct
  (** val double : Z.t -> Z.t **)

  let double = fun x -> Z.(shift_left x 1)

  (** val succ_double : Z.t -> Z.t **)

  let succ_double = fun x -> Z.(add (shift_left x 1) one)

  (** val pred_double : Z.t -> Z.t **)

  let pred_double = fun x -> Z.(sub (shift_left x 1) one)

  (** val pos_sub : int -> int -> Z.t **)

  let rec pos_sub = fun x y -> Z.sub (Z.of_int x) (Z.of_int y)

  (** val add : Z.t -> Z.t -> Z.t **)

  let add = Z.add

  (** val opp : Z.t -> Z.t **)

  let opp = Z.neg

  (** val mul : Z.t -> Z.t -> Z.t **)

  let mul = Z.mul
 end

(** val qmult : Q.t -> Q.t -> Q.t **)

let qmult = Q.mul

(** val qminus : Q.t -> Q.t -> Q.t **)

let qminus = Q.sub

(** val qdiv : Q.t -> Q.t -> Q.t **)

let qdiv = Q.div

(** val hundred : Q.t **)

let hundred = Q.of_int 100

(** val rvc_focused_value : Q.t -> Q.t -> Q.t **)

let rvc_focused_value value_of_good fvnm =
  qmult (qdiv (qminus value_of_good fvnm) value_of_good) hundred

(** val rvc_build_down : Q.t -> Q.t -> Q.t **)

